 package com.myoa.model.timedTaskManagement;
 
 import java.util.Date;
 
 public class TimedTaskRecord
 {
   private Integer ttrId;
   private Integer ttmId;
   private String userId;
   private Date executionTime;
   private Date lishiTime;
   private Integer result;
 
   public Date getLishiTime()
   {
     return this.lishiTime;
   }
 
   public void setLishiTime(Date lishiTime) {
     this.lishiTime = lishiTime;
   }
 
   public Integer getTtrId()
   {
     return this.ttrId;
   }
 
   public void setTtrId(Integer ttrId)
   {
     this.ttrId = ttrId;
   }
 
   public Integer getTtmId()
   {
     return this.ttmId;
   }
 
   public void setTtmId(Integer ttmId)
   {
     this.ttmId = ttmId;
   }
 
   public String getUserId()
   {
     return this.userId;
   }
 
   public void setUserId(String userId)
   {
     this.userId = (userId == null ? null : userId.trim());
   }
 
   public Date getExecutionTime()
   {
     return this.executionTime;
   }
 
   public void setExecutionTime(Date executionTime)
   {
     this.executionTime = executionTime;
   }
 
   public Integer getResult()
   {
     return this.result;
   }
 
   public void setResult(Integer result)
   {
     this.result = result;
   }
 }

