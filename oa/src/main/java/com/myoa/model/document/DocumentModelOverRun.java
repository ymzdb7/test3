 package com.myoa.model.document;
 
 import com.alibaba.fastjson.annotation.JSONField;
 import java.util.Date;
 
 public class DocumentModelOverRun extends DocumentModelWithBLOBs
 {
   String createrName;
   Date endTime;
   Integer realPrcsId;
   Integer step;
 
   public Integer getRealPrcsId()
   {
     return this.realPrcsId;
   }
 
   public void setRealPrcsId(Integer realPrcsId) {
     this.realPrcsId = realPrcsId;
   }
 
   public Integer getStep() {
     return this.step;
   }
 
   public void setStep(Integer step) {
     this.step = step;
   }
 
   public String getCreaterName() {
     return this.createrName;
   }
 
   public void setCreaterName(String createrName) {
     this.createrName = createrName;
   }
   @JSONField(format="yyyy-MM-dd HH:mm:ss")
   public Date getEndTime() { return this.endTime; }
 
   public void setEndTime(Date endTime)
   {
     this.endTime = endTime;
   }
 }

