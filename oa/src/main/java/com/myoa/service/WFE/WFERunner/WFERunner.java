 package com.myoa.service.WFE.WFERunner;
 
 import com.myoa.service.WFE.WFE;

 import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
 
 @Service
 public class WFERunner extends WFE
 {
 
   @Autowired
   protected WFEFlowRun wfeFlowRun;
 
   @Autowired
   protected WFEFlowRunPrcs wfeFlowRunPrcs;
 
   @Autowired
   protected WFEFlowRunInfo wfeFlowRunInfo;
 
   public WFEFlowRun getWfeFlowRun()
   {
     return this.wfeFlowRun;
   }
 
   public void setWfeFlowRun(WFEFlowRun wfeFlowRun) {
     this.wfeFlowRun = wfeFlowRun;
   }
 
   public WFEFlowRunPrcs getWfeFlowRunPrcs() {
     return this.wfeFlowRunPrcs;
   }
 
   public void setWfeFlowRunPrcs(WFEFlowRunPrcs wfeFlowRunPrcs) {
     this.wfeFlowRunPrcs = wfeFlowRunPrcs;
   }
 
   public WFEFlowRunInfo getWfeFlowRunInfo() {
     return this.wfeFlowRunInfo;
   }
 
   public void setWfeFlowRunInfo(WFEFlowRunInfo wfeFlowRunInfo) {
     this.wfeFlowRunInfo = wfeFlowRunInfo;
   }
 }

